package ru.nbdev.cloud.common.messages;

public class ClientAuthMessage extends AbstractMessage {
    private String name;
    private String pass;

    public ClientAuthMessage(String name, String pass) {
        this.name = name;
        this.pass = pass;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}
